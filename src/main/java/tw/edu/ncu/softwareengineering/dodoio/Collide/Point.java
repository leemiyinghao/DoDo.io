package tw.edu.ncu.softwareengineering.dodoio.Collide;

public class Point {
    public double x;
    public double y;

    /**
     * create a point
     * @param x the x coordinate
     * @param y the y coordinate
     */
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
